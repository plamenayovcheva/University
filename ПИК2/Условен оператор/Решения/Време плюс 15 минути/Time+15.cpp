#include <iostream>
#include <string>
using namespace std;
int main()
{
            // Време плюс 15 минути
            int h,m;
            cin>>h;
            cin>>m;
            m = m + 15;
            // Надхвърляме ли 60 минути
            if (m >= 60)
            {
                h += 1;  // h = h + 1;
                m -= 60; // m = m - 60;
            }
            // Надхвърляме ли денонощие
            if (h > 23) h -= 24;
            // Проверка за водеща нула при минути по-малки от 10
            if(m < 10) cout<<h<<":0"<<m;
            else cout<<h<<":"<<m;

system ("pause");
return 0;
}
